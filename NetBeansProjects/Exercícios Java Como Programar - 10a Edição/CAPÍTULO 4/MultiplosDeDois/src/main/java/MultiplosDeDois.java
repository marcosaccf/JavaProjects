// Exercício 4.33: MultiplosDeDois.java
// Exibe na janela de comando os múltiplos do inteiro 2 em um loop infinito.

public class MultiplosDeDois
{
    public static void main(String[] args)
    {
        int i = 2;
        while (i > 0)
        {
            System.out.println(i);
            i *= 2;
        }
    }
}
