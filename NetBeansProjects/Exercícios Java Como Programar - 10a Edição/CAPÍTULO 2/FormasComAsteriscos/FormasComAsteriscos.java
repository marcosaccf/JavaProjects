// Exercício 2.18 - FormasComAsteriscos
// Programa para exibir diversas formas usando asteriscos

public class FormasComAsteriscos
{

    public static void main(String[] args)
    {
        System.out.println( "*********     ***       *        *" );
        System.out.println( "*       *   *     *    ***      * *" );
        System.out.println( "*       *  *       *  *****    *   *" );
        System.out.println( "*       *  *       *    *     *     *" );
        System.out.println( "*       *  *       *    *    *       *" );
        System.out.println( "*       *  *       *    *     *     *" );
        System.out.println( "*       *  *       *    *      *   *" );
        System.out.println( "*       *   *     *     *       * *" );
        System.out.println( "*********     ***       *        *" );
    } // Fim do método principal main
    
} // Fim da classe FormasComAsteriscos.java